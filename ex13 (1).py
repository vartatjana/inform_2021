import turtle
t = turtle.Pen()
t.shape('turtle')
t.color('black')
t.up()
t.goto (100, 0)
t.left(90)
t.down()
t.fillcolor('yellow')
t.begin_fill()
t.circle(100)
t.end_fill()
t.up()
t.goto(-30, 50)
t.down()
t.fillcolor('blue')
t.begin_fill()
t.circle(15)
t.end_fill()
t.up()
t.goto(60, 50)
t.down()
t.fillcolor('blue')
t.begin_fill()
t.circle(15)
t.end_fill()
t.up()
t.goto(0, 30)
t.down()
t.color('black')
t.pensize(8)
t.left(180)
t.forward(50)
t.up()
t.goto(70, -10)
t.color('red')
t.down()
t.circle(-70, 180, 30)
turtle.exitonclick()